# operatorii de comparare dau un tip de date bool

x = 10
y = 12

print('x mai mare decat y ', x > y)
print('x mai mic decat y ', x < y)
print('x este egal decat y ', x == y)
print('x este diferit decat y ', x != y)
print('x este mai mare sau egal ca y ', x >= y)
print('x este mai mic sau egal ca y ', x <= y)