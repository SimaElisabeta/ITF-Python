x = 5
print(x)

x += 2              # x = x + 2
print(x)

x -= 1              # x = x -1
print(x)

x *= 4              # x = x * 4
print(x)

x /= 8              # x = x / 8
print(x)

x //= 3             # x = x // 3
print(x)

x %= 4              # x = x % 4 (% resul impartirii/modulo)
print(x)

x **= 5             # x = x ** 5 (x la puterea a 5-a)
print(x)